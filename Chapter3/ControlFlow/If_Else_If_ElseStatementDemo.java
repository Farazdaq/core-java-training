package Chapter3.ControlFlow;

public class If_Else_If_ElseStatementDemo {
    int mark = 83;

    public  void checkMyMark() {
        if (mark >= 90) {
            System.out.println("A");
        } else if (mark >= 80) {
            System.out.println("B");
        } else if (mark >= 70) {
            System.out.println("C");
        } else  {
            System.out.println("C");
        }
    }

}
